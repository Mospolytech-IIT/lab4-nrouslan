"""Модуль с функцией для деления числа a на число b."""

def divide(a, b):
    """
    Делит a на b.

    Args:
        a (float): Число a.
        b (float): Число b.

    Returns:
    	float: Результат деления a / b.
   
    Raises:
    	ZeroDivisionError: Если b равно 0.
  	"""

    return a / b
